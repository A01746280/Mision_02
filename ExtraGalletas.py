#Este programa calcula la cantidad exacta de ingredientes que se necesitas para hacer determinado número de galletas

num=int(input("¿Cuántas galletas quiere hacer? "))

azucar= ((1.5*num)/48)
mantequilla= ((1*num)/48)
harina= ((2.75*num)/48)

print("La cantidad de azucar que se necesita para hacer", num, "galletas es:", azucar, "tazas")
print("La cantidad de mantequilla que se necesita para hacer", num, "galletas es:", mantequilla, "tazas")
print("La cantidad de harina que se necesita para hacer", num, "galletas es:", harina, "tazas")



        